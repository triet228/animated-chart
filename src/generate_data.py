    # Calculate every possible 40-year trajectory from 1890 to 1986
    for start_year in range(1890, 1987):
        path = [initial_investment]
        val = initial_investment
        for offset in range(1, duration + 1):
            ret = house_price_returns.get(start_year + offset - 1, 4.7) / 100
            val *= (1 + ret)
            path.append(val)
        res[f'Start {start_year}'] = path

    years_held = np.arange(duration + 1)
    res['8%'] = initial_investment * (1.080 ** years_held)
    res['1.5%'] = initial_investment * (1.015 ** years_held)

    res.to_csv(filename, index=False)
    return res

